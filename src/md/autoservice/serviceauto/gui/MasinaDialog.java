    private void jButtonEditReparatieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditReparatieActionPerformed

        int row = jTableReparatii.getSelectedRow();

        if (row != -1) {
            int id = (Integer) reparatiTableModel.getValueAt(row, 0);// 0 -0 prima coloana
            try {
                reparatie = reparatiDao.findById(id);

                List<Integer> listaIdAngajatiSelectati = new ArrayList<>();
                List<Angajat> listaAngajatiSelectati = new ArrayList<>();

                listaIdAngajatiSelectati = reparatieAngajatDao.findAllByIdReparati(reparatie.getId());
                for (Integer idAngajat : listaIdAngajatiSelectati) {
                    angajat = angajatDao.findById(idAngajat);
                    listaAngajatiSelectati.add(angajat);
                }
                reparatie.setListaReparatori(listaAngajatiSelectati);

            } catch (ReparatiNotFoundException ex) {
                Logger.getLogger(MasinaDialog.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Masina nu este gasita cu id=" + id,
                        "Error", JOptionPane.WARNING_MESSAGE);
                return;
            } catch (AngajatNotFoundException ex) {
                Logger.getLogger(MasinaDialog.class.getName()).log(Level.SEVERE, null, ex);
            }

            showReparatiiDialog();
        } else {
            JOptionPane.showMessageDialog(this, "Selectati reparatia!",
                    "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEditReparatieActionPerformed

