    void refreshMasiniTable() {
        //  System.out.println("Id masina =  " + masina.getId());
        reparatiTableModel = new ReparatiTableModel(masina.getId());
        jTableReparatii.setModel(reparatiTableModel);
        jTableReparatii.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    private void fillForm() {
        if (masina.getId() != 0) {

            jTextFieldNrInmatriculare.setText(masina.getNrInmatriculare());
            jTextFieldMarca.setText(masina.getMarca());
            jTextFieldModel.setText(masina.getModel());
            jSpinnerAnu.setValue(masina.getAnu());
            jComboBoxCombustibil.setSelectedItem(masina.getCombustibil());
            jTextFieldModelMotor.setText(masina.getModelMotor());

            if (masina.getProprietar() != null) {
                jTextFieldProprietarNume.setText(masina.getProprietar().getNume());
                jTextFieldProprietarPrenume.setText(masina.getProprietar().getPrenume());
                jTextFieldProprietarTelefon.setText(String.valueOf(masina.getProprietar().getNrTelefon()));
                jTextFieldEmail.setText(masina.getProprietar().getEmail());
            }

        }
    }

    private void getDateMasinaProprietar() {

        proprietar.setNume(jTextFieldProprietarNume.getText());
        proprietar.setPrenume(jTextFieldProprietarPrenume.getText());
        proprietar.setNrTelefon(new Integer(jTextFieldProprietarTelefon.getText()));
        proprietar.setEmail(jTextFieldEmail.getText());

        masina.setNrInmatriculare(jTextFieldNrInmatriculare.getText());
        masina.setMarca(jTextFieldMarca.getText());
        masina.setModel(jTextFieldModel.getText());
        masina.setAnu((int) jSpinnerAnu.getValue());
        masina.setCombustibil((String) jComboBoxCombustibil.getSelectedItem());
        masina.setModelMotor(jTextFieldModelMotor.getText());
        masina.setProprietar(proprietar);

    }

