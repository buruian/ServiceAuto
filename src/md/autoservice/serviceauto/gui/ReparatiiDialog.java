    private void setModels() {

        SpinnerDateModel model = new SpinnerDateModel(new Date(), null,
                null, Calendar.HOUR_OF_DAY);

        jSpinnerReparatieIntrare.setModel(model);

        SpinnerDateModel model1 = new SpinnerDateModel(new Date(), null,
                null, Calendar.HOUR_OF_DAY);
        jSpinnerReparatieIesire.setModel(model1);

        angajatiSelectatiTableModel = new AngajatiSelectatiTableModel();
        jTableAngajatiSelectati.setModel(angajatiSelectatiTableModel);

    }

    private void fillForm() {
        if (reparati.getId() > 0) {

            jSpinnerReparatieIntrare.setValue(reparati.getDataIntrare());

            jSpinnerReparatieIesire.setValue(reparati.getDataIesire());

            jTextAreaProbleme.setText(reparati.getProbleme());
        }
    }

    private void fillTable() {
        angajatiSelectatiTableModel = new AngajatiSelectatiTableModel(reparati.getListaReparatori());
        jTableAngajatiSelectati.setModel(angajatiSelectatiTableModel);
    }

    private void getDateReparatie(int idRepeparatie) {
        //pregatim  reparatia de update
        reparati.setDataIntrare((Date) jSpinnerReparatieIntrare.getValue());
        reparati.setDataIesire((Date) jSpinnerReparatieIesire.getValue());
        reparati.setProbleme(jTextAreaProbleme.getText());
        reparati.setMasina(masina);
        reparati.setListaReparatori(this.angajatiSelectatiTableModel.getAllByReparatieId(idRepeparatie));

    }

    void refreshAngajatiSelectatiTableModel(List<Angajat> listaAngajatiSelectati) {

        angajatiSelectatiTableModel = new AngajatiSelectatiTableModel(listaAngajatiSelectati);
        jTableAngajatiSelectati.setModel(angajatiSelectatiTableModel);

    }

