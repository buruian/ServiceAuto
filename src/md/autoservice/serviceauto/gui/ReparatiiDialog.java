    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String word = "";

        int idRepeparatie = 0;
        try {

            if (reparati.getId() < 1) {//save add new reaparatie  new Reparatie() a venit cu
                getDateReparatie(idRepeparatie);
                reparatiDao.save(reparati);
                idRepeparatie = MyUtil.getMaxId(MyDataSource.getInstace().getConnection(), "reparati");
                word = "adaugat";
                reparati.setId(idRepeparatie);
            } else {
                word = "editat cu";
            }

            idRepeparatie = reparati.getId();

            getDateReparatie(idRepeparatie);
            //1. sterge toati angajatii pentru repartia data
            // 2.  adauga ca sus
            reparatieAngajatDao.update(idRepeparatie, reparati.getListaReparatori());

            // update
            reparatiDao.update(reparati);

            JOptionPane.showMessageDialog(this, "Reparatie " + word + " success!", "Info",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            Logger.getLogger(ReparatiiDialog.class.getName()).log(Level.SEVERE, null, ex);

            JOptionPane.showMessageDialog(this, "Reparatie " + ex.getMessage(),
                    "Error", JOptionPane.WARNING_MESSAGE);
        }

        parentFrame.refreshMasiniTable();

        angajatiSelectatiTableModel.clear();
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

